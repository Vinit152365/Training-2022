--####################################################
--INNER JOIN
--####################################################

SELECT B.ID, B.TITLE, A.FIRST_NAME, A.LAST_NAME
FROM BOOKS B INNER JOIN AUTHORS A
    ON B.AUTHOR_ID= A.ID
ORDER BY B.ID;


SELECT B.ID, B.TITLE, T.FIRST_NAME, T.LAST_NAME
FROM BOOKS B INNER JOIN TRANSLATORS T
    ON B.TRANSLATOR_ID= T.ID
ORDER BY B.ID;

--####################################################
--LEFT JOIN
--####################################################

SELECT B.ID, B.TITLE, B.TYPE, A.LAST_NAME AS AUTHOR, 
       T.LAST_NAME AS TRANSLATOR
FROM BOOKS B LEFT JOIN  AUTHORS A
     ON B.AUTHOR_ID = A.ID
     LEFT JOIN TRANSLATORS T
     ON B.TRANSLATOR_ID = T.ID
ORDER BY B.ID;


SELECT B.ID, B.TITLE, E.LAST_NAME AS EDITOR
FROM BOOKS B LEFT JOIN EDITORS E
     ON B.EDITOR_ID = E.ID
ORDER BY B.ID;

--####################################################
--RIGHT JOIN
--####################################################

SELECT B.ID, B.TITLE, E.LAST_NAME AS EDITOR
FROM BOOKS B RIGHT JOIN EDITORS E
     ON B.EDITOR_ID = E.ID
ORDER BY B.ID;

--####################################################
--FULL JOIN
--####################################################

SELECT B.ID, B.TITLE, E.LAST_NAME AS EDITOR
FROM BOOKS B FULL JOIN EDITORS E
     ON B.EDITOR_ID = E.ID
ORDER BY B.ID;


SELECT B.ID, B.TITLE, A.LAST_NAME AS AUTHOR, E.LAST_NAME AS EDITOR
FROM BOOKS B FULL JOIN AUTHORS A
    ON B.AUTHOR_ID = A.ID
    FULL JOIN EDITORS E
    ON B.EDITOR_ID = E.ID
    FULL JOIN TRANSLATORS T
    ON B.TRANSLATOR_ID = T.ID
ORDER BY B.ID;


